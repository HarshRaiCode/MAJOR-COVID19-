const mongoose= require('mongoose');

const employeeSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    feedback: {
        type: String,
        required: true
    }
})

const Register = new mongoose.model('Feedback', employeeSchema );
module.exports = Register;